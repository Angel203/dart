    private void fill_players() {
        p1.removeAllItems();
        p2.removeAllItems();
        p3.removeAllItems();
        p4.removeAllItems();
        p5.removeAllItems();
        p6.removeAllItems();

        p1.setVisible(false);
        p2.setVisible(false);
        p3.setVisible(false);
        p4.setVisible(false);
        p5.setVisible(false);
        p6.setVisible(false);

        switch (amount) {
            case 6:
                p6.setVisible(true);
            case 5:
                p5.setVisible(true);
            case 4:
                p4.setVisible(true);
            case 3:
                p3.setVisible(true);
            case 2:
                p2.setVisible(true);
            case 1:
                p1.setVisible(true);
                break;
            default:
                System.out.println("Zahl nicht implementiert");
                break;
        }

        // fill in the data:
        p1.setModel(new javax.swing.DefaultComboBoxModel<>(options));

    }

    private void receiveData() {

        playerList = DatabaseController.getAllPlayers();
        options = new String[playerList.size()];
        for(int i=0; i< playerList.size(); i++)
        {
            options[i] = playerList.get(i).getNickname();
        }
    }

